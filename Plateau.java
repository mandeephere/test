/**
 * This class describes a Plateau (Rectangular in this case)
 * @author MANDEEP
 */
public class Plateau {
	
	// Attributes
	private int xMin = 0;
	private int yMin = 0;
	private int xMax = 0;
	private int yMax = 0;
	
	// Getters and Setters
	
	public int getXMin() {
		return xMin;
	}
	public void setXMin(int min) {
		xMin = min;
	}
	public int getYMin() {
		return yMin;
	}
	public void setYMin(int min) {
		yMin = min;
	}
	public int getXMax() {
		return xMax;
	}
	public void setXMax(int max) {
		xMax = max;
	}
	public int getYMax() {
		return yMax;
	}
	public void setYMax(int max) {
		yMax = max;
	}
}
